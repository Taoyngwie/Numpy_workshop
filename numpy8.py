#ตอนที่ 11 - Zeros Matrix

import numpy as np

#สร้าง Zeros Matrix 1D
arr1 = np.zeros(5)
#แสดงอาเรย์
print(arr1)

#สร้าง Zeros Matrix 1D กำหนดเป็นชนิด int
arr1 = np.zeros(5,dtype="int")
#แสดงอาเรย์
print(arr1)

#สร้าง Zeros Matrix 2D
arr2 = np.zeros((2,2))
#แสดงอาเรย์
print(arr2)

arr2 = np.zeros((3,4))
#แสดงอาเรย์
print(arr2)